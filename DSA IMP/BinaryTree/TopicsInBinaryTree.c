/*
List of functions implemented:
1. createNode
2. insert
3. search
4. inorderTraversal
5. preorderTraversal
6. postorderTraversal
7. findMin
8. findMax
9. deleteNode
10. height

Additional possible operations for binary trees:

### Basic Operations:
11. levelOrderTraversal        - Traverse the tree level by level (breadth-first)
12. countNodes                 - Count the total number of nodes in the tree
13. countLeaves                - Count the number of leaf nodes (nodes without children)
14. countInternalNodes         - Count the number of internal nodes (nodes with at least one child)
15. isBST                      - Check if the tree is a valid Binary Search Tree
16. findPredecessor            - Find the in-order predecessor of a node
17. findSuccessor              - Find the in-order successor of a node
18. findDepth                  - Find the depth (distance from the root) of a specific node
19. printPaths                 - Print all root-to-leaf paths
20. mirrorTree                 - Create a mirror image of the binary tree
21. checkIdenticalTrees        - Check if two binary trees are identical
22. printBoundaryNodes         - Print boundary nodes of the tree in a counterclockwise direction

### Intermediate Operations:
23. findAncestors              - Find and print all ancestors of a given node
24. lowestCommonAncestor (LCA) - Find the lowest common ancestor of two nodes
25. findKthSmallest            - Find the kth smallest element in a BST (inorder traversal)
26. findKthLargest             - Find the kth largest element in a BST (reverse inorder traversal)
27. checkBalancedTree          - Check if the tree is balanced (height difference between left and right is <= 1)
28. isSymmetric                - Check if the tree is symmetric (mirror image of itself)
29. findDiameter               - Find the diameter (longest path between any two nodes) of the tree
30. pruneTree                  - Remove subtrees with a sum less than a given value
31. checkCompleteTree          - Check if the tree is a complete binary tree
32. flattenTree                - Flatten the tree to a linked list (inorder traversal)
33. zigzagTraversal            - Traverse the tree in a zigzag (spiral) order
34. verticalOrderTraversal     - Traverse the tree vertically (column by column)

### Advanced Operations:
35. convertToDoublyLinkedList  - Convert the binary tree to a doubly linked list
36. serializeTree              - Serialize the binary tree to a string
37. deserializeTree            - Deserialize the string back into a binary tree
38. checkFullBinaryTree        - Check if the tree is a full binary tree (each node has 0 or 2 children)
39. checkPerfectBinaryTree     - Check if the tree is a perfect binary tree (all internal nodes have 2 children, leaves at the same level)
40. convertToSumTree           - Convert the tree into a sum tree (each node contains the sum of its children)
41. buildTreeFromTraversals    - Reconstruct a tree from given inorder and preorder/postorder traversals
42. checkSubtree               - Check if a tree is a subtree of another binary tree
43. findAllPathsRootToLeaf     - Find and print all paths from the root to the leaf nodes
*/
